package w3schools.Tutorial;

public class PrintNumber {
    public static void main(Strings[] args){
        System.out.println(10);
        System.out.println(10.5);
        System.out.println(10 + 5);
        System.out.println(10 - 5);
        System.out.println(10 * 5);
        System.out.println(10 / 5);
        System.out.println(10 % 5);
    }
}
// 10
// 10.5
// 15
// 5
// 50
// 2
// 0